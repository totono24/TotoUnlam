
public class Director extends Empleado {
	
	private Double sueldoextra;
	private String cochera;
	
	public Director() {
		super();
	}
	
	public Double getSueldoextra() {
		return sueldoextra;
	}
	public void setSueldoextra(Double sueldoextra) {
		this.sueldoextra = sueldoextra;
	}
	public String getCochera() {
		return cochera;
	}
	public void setCochera(String cochera) {
		this.cochera = cochera;
	}
	
}
